package Encapsulation.object;

public class Dog {
private String name;
private String color;
private int age;
private String breed;
public Dog(String name, String color, int age, String breed) {
	this.name = name;
	this.color = color;
	this.age = age;
	this.breed = breed;
}
public String getname() {
	return name;
}
public String getcolor() {
	return color;
}
public int getage() {
	return age;
}
public String getbreed() {
	return breed;
}






}
